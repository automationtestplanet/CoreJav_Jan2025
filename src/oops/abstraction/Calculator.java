package oops.abstraction;

public interface Calculator {

	/**
	 * Description: THis method performs addition on two variables and returns the
	 * result
	 * 
	 * @author RAJU CHELLE
	 * @param a
	 * @param b
	 * @return int value
	 */
	public int addition(int a, int b);

	/**
	 * Description: THis method performs Subtraction on two variables and returns
	 * the result
	 * 
	 * @author RAJU CHELLE
	 * @param a
	 * @param b
	 * @return int value
	 */
	public int subtraction(int a, int b);

	/**
	 * Description: THis method performs Multiplication on two variables and returns
	 * the result
	 * 
	 * @author RAJU CHELLE
	 * @param a
	 * @param b
	 * @return int value
	 */
	public int mulplication(int a, int b);

	/**
	 * Description: THis method performs Division on two variables and returns the
	 * result
	 * 
	 * @author RAJU CHELLE
	 * @param a
	 * @param b
	 * @return int value
	 */
	public int division(int a, int b);

	/**
	 * Description: THis method performs Modular Division on two variables and
	 * returns the result
	 * 
	 * @author RAJU CHELLE
	 * @param a
	 * @param b
	 * @return int value
	 */
	public int modDivision(int a, int b);

}
